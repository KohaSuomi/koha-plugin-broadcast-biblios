let currentLang = 'en';

export function setLang(lang) {
  if (['en', 'fi', 'sv'].includes(lang)) {
    currentLang = lang;
  } else {
    currentLang = 'en';
  }
}

export function t(key) {
  if (translations[key]) {
    return translations[key][currentLang] || translations[key]['en'] || key;
  }
  return key;
}
